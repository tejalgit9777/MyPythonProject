# Logical Operators (And, Or, Not)
# And --> When both conditions are true -- > True otherwise False
# Or --> If one  of the condition is true --> True otherwise False
# Not --> Reverse output

a=3
b=5

print(a>0 and b>0)
print(a<0 and b<0)
print(a<10 and b>10)
print(a>15 and b<2)
print(a>0 or b<0)
print(a<0 or b<0)
print(a<10 or b>10)
print(a>15 or b<2)
print(not a>0)
print(not b<0)