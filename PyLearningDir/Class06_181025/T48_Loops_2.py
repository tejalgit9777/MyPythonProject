
#start from 1
for i in range(1,10,1):
    print("Shine Star!", i)
print("\n")

#start from 2 and end with 8. step=2
for j in range(2,8,2):
    print("Shine Star!", j)
print("\n")

#start from 10, stop=2, step=-1 Reverse
for k in range(10,2,-1):
    print("Shine Star!",k)
print("\n")

#default start is 0 and step is 1
for l in range(10):
    print("Shine Star!",l)
print("\n")

#print in negative numbers
for m in range(-1,-10,-1):
    print("Shine Star!",m)
print("\n")

# give start and stop only
for n in range(3,7):
    print("Shine Star!",n)
print("\n")




