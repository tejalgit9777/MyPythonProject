# Prebuilt functions
# Find max number function
# max (*arg) supports unlimited arguments
result = max(33,67,101,999,-11,11)
print(result)

a = 15
b = 77
result = max(a,b)
print(result)

# min(*arg) find minimum number

result = min(a,b)
print(result)

result = min (-111,-1.5,-0.5,0.25,45,55)
print(result)

