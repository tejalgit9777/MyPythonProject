# Usage of Break, Continue and Pass

print("Pass keyword")
for i in range(0,5):
    if i == 2:
        pass
    print(i)

print("Continue keyword")

for j in range(1,6):
    if j==3:
       continue
    print(j)

print("Break keyword")

for k in range(1,11):
    if k == 7:
        break
    print(k)


